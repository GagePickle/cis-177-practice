// Relation or COMPARISON OPERATORS always yield BOOLEANS
console.log(1 === 2);

// To check is 2 things are considered to be equal...
// '===' is STRICT EQUALITY
// It hecks the VALUE and TYPE
console.log(2 === "2"); // false
console.log(2 === 2); // true
console.log(2 === 3); // false
console.log(2 !== 3); // true
